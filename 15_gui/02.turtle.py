###################################Turtle#########################################

# Import all contents from turtle package
from turtle import *

# Set the width of brush
width(4)

# Go forward
forward(200)
# Turn right 90 degrees:
right(90)

# Set the color of brush:
pencolor('red')
forward(100)
right(90)

pencolor('green')
forward(200)
right(90)

pencolor('blue')
forward(100)
right(90)

# Calling done() makes the window wait for the window to be closed, 
# otherwise the window will be closed immediately:
done()
