##########################################What is Matplotlib?########################################
# Matplotlib is a popular Python package used to build plots. 
# Matplotlib started as a project in the early 2000's partly to use Python to visualize the electronic signals in the brain of epilepsy patients. 
# Matplotlib's creator, John D. Hunter, was a neurobiologist. He was looking for a way to replicate MATLAB's plotting capability with Python. 
# In addition to starting Matplotlib, Dr. Hunter was part of the founding group that created Numfocus. 
# The Numfocus group oversees some major Python projects including Matplotlib, NumPy, Pandas, and Jupyter.


##########################################What is Matplotlib?########################################
